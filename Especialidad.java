/**
 * Define las especialidades médicas disponibles en el sistema.
 */
public enum Especialidad {

    /**
     * Medicina general para pacientes de todas las edades.
     */
    GENERAL,

    /**
     * Atención médica especializada en pacientes pediátricos.
     */
    PEDIATRIA,

    /**
     * Diagnóstico y tratamiento de afecciones de la piel.
     */
    DERMATOLOGIA,

    /**
     * Atención de lesiones óseas y musculoesqueléticas.
     */
    TRAUMATOLOGIA,

    /**
     * Otras especialidades no listadas explícitamente.
     */
    OTROS;
}